// Lookup function
export function lookupHanViet(kanji: string): HanVietEntry | undefined {
  return HAN_VIET_DICT[kanji];
}

// Decompose a word into its Hán Việt components
export function decomposeHanViet(word: string): Array<{ char: string; hanViet: string | null }> {
  return word.split('').map(char => {
    const entry = HAN_VIET_DICT[char];
    return { char, hanViet: entry?.hanViet || null };
  });
}

// Get Hán Việt reading for a compound word
export function getCompoundHanViet(word: string): string {
  return word.split('').map(char => {
    const entry = HAN_VIET_DICT[char];
    return entry?.hanViet || char;
  }).join(' ');
}
